public class Praktikum2_3 {
    public static void main(String[] args) {
        //String Awal
        String teks = "Saya sedang belajar bahasa java di UMN";
        
        //Mengonversi string menjadi huruf kapital (uppercase)
        String uppercaseTeks = teks.toUpperCase();

        //Menampilkan hasilnya
        System.out.println("String asli: " + teks);
        System.out.println("String dalam huruf kapital: " + uppercaseTeks); 
    }
}
